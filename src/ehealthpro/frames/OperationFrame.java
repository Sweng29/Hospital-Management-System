    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (chargesTextfield.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please fill charges field to add record.");
        }else if(operationTypeTextfield.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please fill operation type field to add record.");
        }
        else {
            String charges = chargesTextfield.getText();
            currentDate = new Timestamp(System.currentTimeMillis());
            String operationType = operationTypeTextfield.getText();
            OperationModel operationModel = new OperationModel();
            operationModel.setType(operationType);
            operationModel.setCreatedBy(1);
            operationModel.setCreatedDate(currentDate);
            operationModel.setModifiedBy(1);
            operationModel.setModifiedDate(currentDate);
            operationModel.setCharges(charges);
            OperationDAOImpl operationDAOImpl = new OperationDAOImpl();
            int result = operationDAOImpl.addOperation(operationModel);
            if (result > 0) {
                JOptionPane.showMessageDialog(this, result + " record has been successfully added.");
                fillOperationTable();
            } else {
                JOptionPane.showMessageDialog(this, result + " record could not be added,try again.");
            }
            clearAllFields();
            resetAddButton();
        }
    }//GEN-LAST:event_addButtonActionPerformed

