    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        String charges = chargesTextfield.getText();
        String operationType = operationTypeTextfield.getText();
        currentDate = new Timestamp(System.currentTimeMillis());
        OperationModel operationModel = new OperationModel();
        operationModel.setCharges(charges);
        operationModel.setType(operationType);
        operationModel.setModifiedBy(2);
        operationModel.setModifiedDate(currentDate);
        operationModel.setOperationId(operationId);
        OperationDAOImpl operationDAOImpl = new OperationDAOImpl();
        int result = operationDAOImpl.updateOperation(operationModel);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, result + " record has been successfully updated.");
            fillOperationTable();
        } else {
            JOptionPane.showMessageDialog(this, result + " record could not be updated,try again.");
        }
        clearAllFields();
        resetAddButton();
    }//GEN-LAST:event_updateButtonActionPerformed

